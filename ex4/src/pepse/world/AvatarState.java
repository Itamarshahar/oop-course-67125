package pepse.world;

/**
 * Enum representing the different states of the Avatar.
 */
public enum AvatarState {
    /**
     * State when the Avatar is moving left.
     */
    LEFT,

    /**
     * State when the Avatar is moving right.
     */
    RIGHT,

    /**
     * State when the Avatar is jumping.
     */
    JUMP,

    /**
     * State when the Avatar is resting (idle).
     */
    REST,

    /**
     * State when the Avatar is flying.
     */
    FLY
}
